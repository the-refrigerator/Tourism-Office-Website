export function getHostpotIcon(icon) {
  switch (icon) {
    case "highest-peak":
      return "hotspots/highest-peak.png";
    case "rain":
      return "hotspots/rain.png";
    case "storm":
      return "hotspots/storm.png";
    case "meteor-showers":
      return "hotspots/meteor-showers.png";
    case "ocean":
      return "hotspots/ocean.png";
    case "wind":
      return "hotspots/wind.png";
    case "volcano":
      return "hotspots/volcano.png";
    case "icy-region":
      return "hotspots/icy-region.png";
    case "precious-materials":
      return "hotspots/precious-materials.png";
    case "moons":
      return "hotspots/moons.png";
    case "crater":
      return "hotspots/crater.png";
    case "dune":
      return "hotspots/dune.png";
    case "canyon":
      return "hotspots/canyon.png";
    case "first-landing-spot":
      return "hotspots/first-landing-spot.png";
    case "hot-region":
      return "hotspots/hot-region.png";
    case "attraction":
      return "hotspots/attraction.png";
  }
  return "hotspots/attraction.png";
}
